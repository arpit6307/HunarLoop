  static String getFallbackLocale(String language) {
    final lang = language.toUpperCase().trim();
    const supported = {
      'ENGLISH', 'HINDI', 'BENGALI', 'MARATHI', 'TELUGU', 'TAMIL', 'GUJARATI', 'URDU', 'KANNADA', 'PUNJABI', 'MALAYALAM', 'ODIA'
    };
    if (supported.contains(lang)) {
      return lang;
    }
    if (const ['MAITHILI', 'BHOJPURI', 'MAGAHI', 'VAJJIKA', 'CHHATTISGARHI', 'GONDI', 'HALBI', 'HARYANVI', 'PAHARI', 'KANGRI', 'DOGRI', 'KINNAURI', 'BUNDELI', 'MALVI', 'NIMADI', 'RAJASTHANI', 'MARWARI', 'SHEKHAWATI', 'MEWARI', 'AWADHI', 'BRAJ BHASHA', 'GARHWALI', 'KUMAONI', 'NEPALI'].contains(lang)) {
      return 'HINDI';
    }
    if (const ['ASSAMESE', 'BODO', 'MEITEI (MANIPURI)', 'MANIPURI', 'TANGKHUL', 'KOKBOROK', 'SANTALI'].contains(lang)) {
      return 'BENGALI';
    }
    if (const ['KONKANI'].contains(lang)) {
      return 'MARATHI';
    }
    if (const ['SINDHI', 'KUTCHI'].contains(lang)) {
      return 'GUJARATI';
    }
    if (const ['TULU'].contains(lang)) {
      return 'KANNADA';
    }
    if (const ['LAMBADI'].contains(lang)) {
      return 'TELUGU';
    }
    if (const ['KASHMIRI'].contains(lang)) {
      return 'URDU';
    }
    return 'ENGLISH';
  }
