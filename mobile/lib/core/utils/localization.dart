  static String translate(String key, String locale) {
    final values = _localizedValues[locale.toUpperCase()] ?? _localizedValues['ENGLISH']!;
    return values[key] ?? key.toUpperCase();
  }

  static String translateCategory(String categoryName, String locale) {
    final key = 'cat_' + categoryName.toLowerCase().replaceAll(' & ', '_').replaceAll(' / ', '_').replaceAll(' ', '_');
    final trans = translate(key, locale);
    if (trans == key.toUpperCase()) {
      return categoryName;
    }
    return trans;
  }

